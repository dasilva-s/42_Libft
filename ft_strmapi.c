#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	size_t			l;
	unsigned int	i;

	i = 0;
	l = ft_strlen(s);
	if (!s)
		return (0);
	str = malloc((l + 1) * sizeof(char));
	while (str[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
